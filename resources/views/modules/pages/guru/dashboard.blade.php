    <script type="text/javascript">
        document.addEventListener('DOMContentLoaded', function() {
            var ctx = document.getElementById('nilaiChart').getContext('2d');
            var nilaiData = {!! json_encode($nilai) !!};

            var labels = nilaiData.map(function(item) {
                return item.nama_siswa;
            });
            var data = nilaiData.map(function(item) {
                return item.total_nilai;
            });

            var chart = new Chart(ctx, {
                type: 'bar', // Gunakan 'horizontalBar' untuk chart horizontal
                data: {
                    labels: labels,
                    datasets: [{
                        label: '5 Siswa Terbaik',
                        data: data,
                        backgroundColor: 'rgba(75, 192, 192, 0.2)',
                        borderColor: 'rgba(75, 192, 192, 1)',
                        borderWidth: 1
                    }]
                },
                options: {
                    scales: {
                        y: {
                            beginAtZero: true
                        }
                    }
                }
            });
        });
